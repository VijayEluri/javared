    public void InitializeConnection()
    {
        database = DataHandler.database;
        this.cmbCompany.removeAllItems();
        CompanyList = new ArrayList();
        try
        {
            if(DataHandler.error==true){
                throw new Exception("Unable to load Configuration file.");
            }
            System.out.println("Attempting Connection: " + DataHandler.GetConURL());
            rwframework.models.Company Companies = new rwframework.models.Company(this.DataHandler);
            ArrayList myResults = Companies.find_all("application = 'rmms'","name ASC");
            for(int x=0;x<myResults.size();x++)
            {
                ArrayList ResultsList = new ArrayList();
                rwframework.controls.DCOMActiveRecord rst = (rwframework.controls.DCOMActiveRecord)(myResults.get(x));
                String CompanyName;
                CompanyName = rst.getValue("name");
                ResultsList.add(rst.getValue("id"));
                ResultsList.add(CompanyName);
                ResultsList.add(rst.getValue("catalog_name"));
                ResultsList.add(rst.getValue("address"));
                ResultsList.add(rst.getValue("address_2"));
                ResultsList.add(rst.getValue("city"));
                ResultsList.add(rst.getValue("state"));
                ResultsList.add(rst.getValue("zip"));
                ResultsList.add(rst.getValue("phone"));
                CompanyList.add(ResultsList);
                if(x==0)
                    DataHandler.database = rst.getValue("catalog_name");
                this.cmbCompany.addItem(CompanyName);
                
                
            }
            
            cmdlogin.setEnabled(true);
        } catch (SQLException ex)
        {
            System.out.println("SQLException: " + ex.getMessage());
            System.out.println("SQLState: " + ex.getSQLState());
            System.out.println("VendorError: " + ex.getErrorCode());
            cmdlogin.setEnabled(false);
            JOptionPane.showMessageDialog(this,"There was an error connecting to the database, please check configuration.");
        }catch(Exception e){
            System.out.println(e.toString());
            cmdlogin.setEnabled(false);
            JOptionPane.showMessageDialog(this,"The configuation file is corrupt or missing, please re-configure.");
        }
    }
