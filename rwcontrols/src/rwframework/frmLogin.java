    private void cmdloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdloginActionPerformed
// TODO add your handling code here:
        rwframework.models.User MainUser;
        cDataHandler newDataHandler = new cDataHandler(DataHandler.ErrorHandler);
        try{
        newDataHandler.loadSettings("config.xml");
        }
        catch(Exception e)
        {
         DataHandler.ErrorHandler.printLog("frmLogin","cmdloginActionPerformed()",e.toString());   
        }
        newDataHandler.database = DataHandler.database;

        MainUser = new rwframework.models.User(newDataHandler);
        if(MainUser.Validate(this.txtusername.getText(),this.txtpassword.getText()))
        {
            this.setVisible(false);
            rwframework.models.Company CurrentCompany = new rwframework.models.Company(DataHandler);
            CurrentCompany.setValue("id",((ArrayList)CompanyList.get(cmbCompany.getSelectedIndex())).get(0).toString());
            CurrentCompany.load();
            Application MainFrame = new Application(newDataHandler,CurrentCompany,MainUser);
          //  frmMainFrame MainFrame = new frmMainFrame(DataHandler,MainUser,(String)this.cmbCompany.getSelectedItem());
          
            MainFrame.setExtendedState(JFrame.MAXIMIZED_BOTH);
            MainFrame.setVisible(true);
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Invalid Username/Password...Try Again!");
            this.txtpassword.setSelectionStart(0);
            this.txtpassword.setSelectionEnd(this.txtpassword.getText().length());
            this.txtpassword.requestFocus();
            try
            {
            newDataHandler.GetConnection().close();
            }
            catch(SQLException ex)
            {
                
            }
        }
        
    }//GEN-LAST:event_cmdloginActionPerformed

